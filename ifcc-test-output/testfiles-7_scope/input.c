int main() {
    int a; a = 100;
    int b; b = 2;
    int res;

    {
        int a; a = 10;  // 遮蔽外部的 a (100)
        b = b * a;      // 这里的 b 是外部的，b 变为 20
        {
            int a; a = 5;       // 再次遮蔽 a (10)
            int b; b = 1;       // 遮蔽外部的 b (20)
            a = a + b + 10;     // 这里的 a 变成 16
        }
        // 离开内层块，此时 a 应该恢复为 10
        a = a + 5;      // a 变成 15
    }

    // 预期结果: 外部 a (100) + 外部 b (20) + 中层 a 的残留影响（应为 0，因为它是局部变量）
    // 最终 res = 100 + 20 = 120
    res = a + b;
    return res;
}