int main() {
    int a; int b; int res;
    a = 12; // 二进制: 1100
    b = 7;  // 二进制: 0111
    
    // 挑战点：位运算、位移与比较运算的混合
    // 计算步骤:
    // 1. (a & b) = (1100 & 0111) = 0100 (4)
    // 2. (a ^ b) = (1100 ^ 0111) = 1011 (11)
    // 3. (4 << 2) = 16
    // 4. (11 >> 1) = 5
    // 5. 16 | 5 = 21
    // 6. 21 > 15 结果为 1
    res = ((a & b) << 2 | (a ^ b) >> 1) > 15;

    // 进一步混淆：使用结果进行位反转式的计算
    // 1 != 0 结果为 1，再加一个位运算
    res = (res != 0) & (a >> 3); // 1 & 1 = 1

    return res; // 预期结果: 1
}